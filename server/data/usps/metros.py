class MajorMetros:
    """Simple tool to look up preferred city and state for a given city name."""

    @classmethod
    def for_city(cls, city: str) -> CityState | None:
        """Return the preferred city and state for the given city name."""
        cs = _METROS.get(city.title())
        return CityState(cs.city.upper(), cs.state.upper()) if cs else None
